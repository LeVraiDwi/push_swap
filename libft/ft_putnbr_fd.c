#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;
	long	i;

	i = (long)n;
	if (i < 0)
	{
		i = i * (-1);
		write(fd, "-", 1);
	}
	if (i / 10 != 0)
		ft_putnbr_fd(i / 10, fd);
	c = (i % 10) + '0';
	write(fd, &c, 1);
}
